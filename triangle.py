# Создаем функцию area для нахождения площади треугольника
# Входные параметры: a,b,c - сторона треугольника
# Выходные параметры: (a + b + c) / 2 -  треугольника
def area(a, b, c):
    return (a + b + c) / 2

# Создаем функцию perimeter для нахождения периметра треугольника
# Входные параметры: a,b,c - сторона треугольника
# Выходные параметры:  a + b + c - периметр треугольника
def perimeter(a, b, c):
    return a + b + c
