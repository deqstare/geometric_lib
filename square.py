# Создаем функцию area для нахождения площади квадрата
# Входные параметры: a - сторона квадрата
# Выходные параметры: a * a -  площадь квадрата
def area(a):
    return a * a

# Создаем функцию perimeter для нахождения периметра квадрата
# Входные параметры: a - сторона квадрата
# Выходные параметры: 4 * a -  периметр квадрата
def perimeter(a):
    return 4 * a
