import math
# Импортируем модуль math для введения числа пи в программу

# Создаем функцию area для нахождения площади
# Входные параметры: r - радиус окружности
# Выходные параметры: math.pi * r * r -  площадь окружности

def area(r):
    return math.pi * r * r

# Создаем функцию perimeter для нахождения периметра
# Входные параметры: r - радиус окружности
# Выходные параметры: 2 * math.pi * r -  периметр окружности

def perimeter(r):
    return 2 * math.pi * r

